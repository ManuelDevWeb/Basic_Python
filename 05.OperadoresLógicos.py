estudiante=True
trabaja=False
numero1=10
numero2=10
numero3=20

# Operador and
print("Estudia y trabaja: ", estudiante and trabaja)

# Operador or
print("Estudia o trabaja: ", estudiante or trabaja)

# Operador not
print("No Estudia: ", not estudiante)

# Operador ==
print("Son iguales: ", numero1 == numero2)

# Operador !=
print("Son diferentes: ", numero1 != numero3)

# Operador >
print("Es mayor: ", numero1 > numero3)

# Operador <
print("Es menor: ", numero1 < numero3)

# Operador >=
print("Es mayor o igual: ", numero1 >= numero3)

# Operador <=
print("Es menor o igual: ", numero1 <= numero3)